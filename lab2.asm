NUM_INDEX EQU 10H
BIT_INDEX EQU 11H
ORG 0000H
LJMP MAIN
ORG 0030H
MAIN:
MOV P0,#00H
MOV P2,#00H
MOV R2,#06H
MOV NUM_INDEX,#00H
MOV BIT_INDEX,#00H
LOOP:
LCALL DISPLAY
INC NUM_INDEX
INC BIT_INDEX
DJNZ R2,LOOP
MOV R2,#06H
JMP MAIN

DISPLAY:
MOV A,NUM_INDEX
MOV DPTR,#NUM_TAB
MOVC A,@A+DPTR
MOV P0,A
MOV A,BIT_INDEX
MOV DPTR,#BIT_TAB
MOVC A,@A+DPTR
MOV P2,A
LCALL DELAY
RET

DELAY:MOV R3,#10
D1:MOV R4,#100
D2:MOV R5,#250
DJNZ R5,$
DJNZ R4,D2
DJNZ R3,D1
RET

NUM_TAB:
DB 03FH	;"0"
DB 006H	;"1"
DB 05BH	;"2"
DB 04FH	;"3"
DB 066H	;"4"
DB 06DH	;"5"
DB 07DH	;"6"
DB 007H	;"7"
DB 07FH	;"8"
DB 06FH	;"9"

BIT_TAB:
DB 0FEH
DB 0FDH
DB 0FBH
DB 0F7H
DB 0EFH
DB 0DFH
DB 0BFH
DB 07FH